# Exemplo de Saída de Dados

# Declarando variáveis
numero1 = 10
numero2 = 5

# Realizando uma soma e exibindo o resultado
resultado = numero1 + numero2
print(f"A soma de {numero1} e {numero2} é {resultado}.")


